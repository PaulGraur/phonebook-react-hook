      {/* –ö–æ–º–ø–æ–Ω–µ–Ω—Ç –¥–ª—è —Ñ—ñ–ª—å—Ç—Ä–∞—Ü—ñ—ó –∫–æ–Ω—Ç–∞–∫—Ç—ñ–≤, –ø–µ—Ä–µ–¥–∞—î–º–æ —Å—Ç–∞–Ω —Ñ—ñ–ª—å—Ç—Ä–∞ —ñ —Ñ—É–Ω–∫—Ü—ñ—é –¥–ª—è –π–æ–≥–æ –æ–Ω–æ–≤–ª–µ–Ω–Ω—è */}
